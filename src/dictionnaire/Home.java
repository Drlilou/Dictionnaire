    private boolean valideWord(String word) {

        if (word == null || word.isEmpty()) {
            return false;
        }

        if (!word.matches("[a-zA-Z]+")) {
            return false;
        }

        if (word.isEmpty()) {
            return false;
        }

        return true;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String word = jTextField1.getText();
        if (valideWord(word)) {
            WordBDD wordBDD = new WordBDD(word);

            if (jRadioButton1.isSelected()) {
                boolean b = wordBDD.insertWord();
                if (b) {
                    JOptionPane.showMessageDialog(this, "done");
                } else {
                    JOptionPane.showMessageDialog(this, "deja exist");

                }
            }
            if (jRadioButton2.isSelected()) {
                int sure = JOptionPane.showConfirmDialog(this, "are you sure", "Warning", JOptionPane.YES_NO_OPTION);
                if (sure == 0) {
                    boolean b = wordBDD.deleteWord();
                    if (b) {
                        JOptionPane.showMessageDialog(this, "done");
                    } else {
                        JOptionPane.showMessageDialog(this, "deja exist");

                    }
                }
            }
            if (jRadioButton3.isSelected()) {
                int line = jTable1.getSelectedRow();
                if (line != -1) {
                    String old = jTable1.getModel().getValueAt(line, 0).toString();
                    boolean b = wordBDD.updateWord(old);
                    if (b) {
                        JOptionPane.showMessageDialog(this, "done");
                    } else {
                        JOptionPane.showMessageDialog(this, "deja exist");

                    }
                }
            }
            fillTable();
            jTextField1.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "donnee un mot valide");

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_UP || key == KeyEvent.VK_DOWN) {
            int selectedRow = jTable1.getSelectedRow();
            int rowCount = jTable1.getRowCount();
            if (selectedRow < 0 || selectedRow >= rowCount) {
                return;
            }
            if (key == KeyEvent.VK_UP) {
                selectedRow--;
                if (selectedRow < 0) {
                    selectedRow = rowCount - 1;
                }
            } else if (key == KeyEvent.VK_DOWN) {
                selectedRow++;
                if (selectedRow >= rowCount) {
                    selectedRow = 0;
                }
            }
            jTable1.setRowSelectionInterval(selectedRow, selectedRow);
            this.jTable1MouseClicked(null);
        }
        if (key == KeyEvent.VK_DELETE) {
            int sure = JOptionPane.showConfirmDialog(this, "are you sure", "Warning", JOptionPane.YES_NO_OPTION);
            if (sure == 0) {
                String word = jTextField1.getText();
                WordBDD wordBDD = new WordBDD(word);
                boolean b = wordBDD.deleteWord();
                if (b) {
                    JOptionPane.showMessageDialog(this, "done");
                } else {
                    JOptionPane.showMessageDialog(this, "deja exist");

                }
            }
            fillTable();
            jTextField1.setText("");

        }
    }//GEN-LAST:event_jTable1KeyPressed

