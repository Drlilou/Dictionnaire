    private void fillTable() {
        DefaultTableModel model = new DefaultTableModel();

// add the column headers to the model
        model.addColumn("Dictionnaire");
        try {
            String query = "select * from word Dictionnaire";
            java.sql.PreparedStatement ps = ConnectionBDD.getConnection().prepareStatement(query);
            java.sql.ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Object[] row = new Object[1];
                row[0] = rs.getString("word");
                model.addRow(row);
            }
            jTable1.setModel(model);

// set the cells as uneditable
            jTable1.setDefaultEditor(Object.class, null);

        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

