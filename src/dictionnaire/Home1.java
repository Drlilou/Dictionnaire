    private void fillTable() {
        try {
            String query = "select * from word Dictionnaire";
            java.sql.PreparedStatement ps = ConnectionBDD.getConnection().prepareStatement(query);
            java.sql.ResultSet rs = ps.executeQuery();
            String text = "";
            while (rs.next()) {

                String word = rs.getString("word");
                text += word + "\n";
            }
            jTextArea1.setText(text);

// set the cells as uneditable
        } catch (SQLException ex) {
            Logger.getLogger(Home1.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

